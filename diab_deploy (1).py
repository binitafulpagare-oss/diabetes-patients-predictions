import pandas as pd
import numpy as np
import pickle
import streamlit as st
import joblib

st.title('Diabetes Prediction App (Logistic Regression)')

st.sidebar.header('User Input Parameters')

model = joblib.load("diabetes_model.pkl")
scaler = joblib.load("scaler.pkl")

model = pickle.load(open('diabetes_model.pkl', 'rb'))
scaler = pickle.load(open('scaler.pkl', 'rb'))

def user_input_features():
    pregnancies = st.sidebar.number_input('Pregnancies', min_value=0)
    glucose = st.sidebar.number_input('Glucose', min_value=0)
    bp = st.sidebar.number_input('Blood Pressure', min_value=0)
    skin = st.sidebar.number_input('Skin Thickness', min_value=0)
    insulin = st.sidebar.number_input('Insulin', min_value=0)
    bmi = st.sidebar.number_input('BMI', min_value=0.0)
    dpf = st.sidebar.number_input('Diabetes Pedigree Function', min_value=0.0)
    age = st.sidebar.number_input('Age', min_value=0)

    data = {
        'Pregnancies': pregnancies,
        'Glucose': glucose,
        'BloodPressure': bp,
        'SkinThickness': skin,
        'Insulin': insulin,
        'BMI': bmi,
        'DiabetesPedigreeFunction': dpf,
        'Age': age
    }

    return pd.DataFrame(data, index=[0])

df = user_input_features()

st.subheader('User Input Parameters')
st.write(df)

scaled_data = scaler.transform(df)

prediction = model.predict(scaled_data)
prediction_proba = model.predict_proba(scaled_data)

st.subheader('Prediction Result')

if prediction[0] == 1:
    st.error("Diabetic")
else:
    st.success("Not Diabetic")

st.subheader('Prediction Probability')
st.write(prediction_proba)
